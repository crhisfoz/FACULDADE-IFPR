public class Airport {
    private String name;
    private String city;
    private int alt;
    
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getCity() {
        return city;
    }
    public void setCity(String city) {
        this.city = city;
    }
    public int getAlt() {
        return alt;
    }
    public void setAlt(int alt) {
        this.alt = alt;
    }
    
}